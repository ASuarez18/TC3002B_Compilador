
# Structure
'''
- Alpha (A-Z,a-z)
- Digit  (0-9)
- Tokens
    - Identifiers
        + Letters, digits, undersore (_)
        + Upper & Lower are different
    - Keywords
        - break
        - if
        - elseif
        - else
        - dec
        - do
        - return
        - true
        - false
        - var
        - inc
        - while
    - Literals
        - Booleans
            - true (1)
            - false (0)
        - Integers
            + 0..n digits
            + Range = -2,147,483,648 to 2,147,483,647
        - Characters
        - Strings
    - Operators
        - Arithmetic
            - Minus (-)
                + -x (negate)
                + x - y (substraction)
            - Plus (+)
                - +x (Produces)
                - x + y (Addition)
            - Multiplication (x * y)
            - Division (x / y)
            - Remainder (x % y)
        - Logical
            - Not (!x)
            - And (x && y)
            - Or (x || y)
            - Xor (x ^ y)
        - Comparison and Relational
            - Equal (x == y)
            - Not equal (x != y)
            - Greater (x > y)
            - Less (x < y)
            - Greater Equal (x >= y)
            - Less Equal (x <= y)
        - Function call (f(arg1,..., argn))


    - White space (Delimiters between tokens)
        - Spaces
        - Tabulators
        - Newlines
        - Comments
            - Single (#)
            - Multi-line(<# #>)


'''